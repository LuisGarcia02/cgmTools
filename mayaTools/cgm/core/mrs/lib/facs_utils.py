def iterNameTags(baseName = '',side = ''):
    ml = cgmMeta.asMeta(sl=1)
    if side:
        _baseName = "{0}_{1}".format(side,baseName)
    else:
        _baseName = baseName
    
    if len(ml)==1:
        ml[0].doStore('handleTag', _baseName)
        ml[0].rename("{0}".format(_baseName))        
        return
        
        
    for i,mObj in enumerate(ml):
        mObj.doStore('handleTag', "{0}_{1}".format(_baseName,i))
        mObj.rename("{0}_{1}".format(_baseName,i))
    


